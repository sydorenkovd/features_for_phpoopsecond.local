InnoDB была разработана для обеспечения максимальной производительности при обработке больших объемов данных. Механизм хранения InnoDB поддерживает свой собственный буферный пул для кэширования данных и индексов в основной памяти. InnoDB хранит свои таблицы и индексы в табличном пространстве, которая может состоять из нескольких файлов (или неразмеченные разделы диска). Это отличается от, например, таблиц MyISAM , где каждая таблица хранится с использованием отдельных файлов. InnoDB таблицы может быть очень большим, даже на операционных системах, где размер файла ограничен 2 Гб.


 
 
 
 