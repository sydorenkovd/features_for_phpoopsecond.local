InnoDB является механизмом хранения поддерживающим безопасность транзакций  (ACID совместимый)для MySQL; поддерживает  подтверждение и откат транзакций, восстановление поврежденных данных для пользователя. InnoDB поддерживает блокировки на уровне строк (без использования грубых блокировок) и Oracle-стиль соответствующий не блокирующим чтениям для увеличение многопользовательского параллелизма и производительности. InnoDB хранит данные пользователя в кластерных индексах для уменьшения операций ввода / вывода для общих запросов на основе первичных ключей. Для поддержания целостности данных, InnoDB поддерживает внешние ключи FOREIGN KEY. Вы можете свободно смешивать InnoDB таблиц с таблицами с другими механизмами хранения MySQL, даже в рамках одного выражения.

